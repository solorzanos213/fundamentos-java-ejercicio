package com.example;

public class TiposDeDatosPrimitivos {

    public static void ejemplo() {
        int numero = 10;
        double decimal = 5.5;
        char letra = 'A';
        boolean activo = true;

        System.out.println("Entero: " + numero);
        System.out.println("Decimal: " + decimal);
        System.out.println("Letra: " + letra);
        System.out.println("Booleano: " + activo);
    }

}