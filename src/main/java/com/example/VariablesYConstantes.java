package com.example;

public class VariablesYConstantes {

    public static void ejemplo() {

        // Declaración de variables
        int edad;
        String nombre;

        // Asignación de valores
        edad = 20;
        nombre = "Sofia";

        // Variables con nombres convencionales (camelCase)
        double precioProducto = 15.99;
        int cantidadEstudiantes = 30;

        // Constante (se escribe en MAYÚSCULAS)
        final double PI = 3.1416;

        // Mostrar resultados
        System.out.println("Nombre: " + nombre);
        System.out.println("Edad: " + edad);
        System.out.println("Precio del producto: " + precioProducto);
        System.out.println("Cantidad de estudiantes: " + cantidadEstudiantes);
        System.out.println("Constante PI: " + PI);
    }
}